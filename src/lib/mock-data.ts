// Dados do heatmap de produtividade (√∫ltimos 90 dias)
export function generateHeatmapData(): Record<string, number> {
    const data: Record<string, number> = {};
    const today = new Date();
    for (let i = 0; i < 90; i++) {
        const date = new Date(today);
        date.setDate(date.getDate() - i);
        const key = date.toISOString().split('T')[0];
        // Peso aleat√≥rio com mais chances de dias produtivos em dias de semana
        const dayOfWeek = date.getDay();
        if (dayOfWeek === 0) {
            data[key] = Math.random() > 0.7 ? Math.floor(Math.random() * 2) : 0;
        } else if (dayOfWeek === 6) {
            data[key] = Math.random() > 0.5 ? Math.floor(Math.random() * 3) : 0;
        } else {
            data[key] = Math.floor(Math.random() * 5) + 1;
        }
    }
    return data;
}

